DROP SCHEMA IF EXISTS public CASCADE;
CREATE SCHEMA public;
SET search_path TO public;

CREATE TABLE Roles (
    id_rol      SERIAL PRIMARY KEY,
    rol         VARCHAR(20) NOT NULL
);

CREATE TABLE Users (
    id_usuario      SERIAL PRIMARY KEY,
    id_rol          INTEGER NOT NULL REFERENCES Roles(id_rol),
    contrasena      VARCHAR(100) NOT NULL, 
    nombre_usuario  VARCHAR(100),
    correo          VARCHAR(100)
);

CREATE TABLE Temporadas (
    id_temporada    SERIAL PRIMARY KEY,
    anyo            DATE NOT NULL
);

CREATE TABLE Pabellones (
    id_pabellon     SERIAL PRIMARY KEY,
    nombre_pab      VARCHAR(150) NOT NULL,
    ciudad          VARCHAR(100)
);

CREATE TABLE Posicion (
    id_posicion     SERIAL PRIMARY KEY,
    categoria       VARCHAR(10)  NOT NULL,   --Portero, Campo
    rol_especifico  VARCHAR(20)  NOT NULL
);

CREATE TABLE Equipos (
    id_equipo       INTEGER PRIMARY KEY,
    nombre_equipo   VARCHAR(100) NOT NULL,
    imagen_logo     TEXT,
    ciudad          VARCHAR(100),
    id_pabellon     INTEGER REFERENCES Pabellones(id_pabellon)
);

CREATE TABLE Jugadores (
    id_jugador      INTEGER PRIMARY KEY,
    nombre_jugador  VARCHAR(100) NOT NULL,
    dorsal          INTEGER NOT NULL,
    imagen_jugador  TEXT,
    id_posicion     INTEGER REFERENCES Posicion(id_posicion),
    id_equipo       INTEGER REFERENCES Equipos(id_equipo)
);

CREATE TABLE Partidos (
    id_partido          INTEGER PRIMARY KEY,
    id_equipo_local     INTEGER NOT NULL REFERENCES Equipos(id_equipo),
    id_equipo_visitante INTEGER NOT NULL REFERENCES Equipos(id_equipo),
    id_temporada        INTEGER NOT NULL REFERENCES Temporadas(id_temporada),
    id_pabellon         INTEGER REFERENCES Pabellones(id_pabellon),
    jornada             INTEGER,
    fecha               DATE,
    hora                TIME,
    goles_local         INTEGER DEFAULT 0,
    goles_visitante     INTEGER DEFAULT 0,
    condicion           VARCHAR(20)          -- local o visitante
);

CREATE TABLE Situacion_Juego (
    id_situacion    SERIAL PRIMARY KEY,
    descripcion     VARCHAR(100)
);

CREATE TABLE Estadisticas_Base (
    id_base             INTEGER PRIMARY KEY,
    id_jugador          INTEGER NOT NULL REFERENCES Jugadores(id_jugador),
    id_partido          INTEGER NOT NULL REFERENCES Partidos(id_partido),
    id_situacion        INTEGER REFERENCES Situacion_Juego(id_situacion),
    tiempo_def          VARCHAR(10),
    tiempo_tot          VARCHAR(10),
    valoracion_global   NUMERIC(5,1) DEFAULT 0,
    sancion_amarilla    INTEGER DEFAULT 0,
    sancion_2mins_1     INTEGER DEFAULT 0,
    sancion_2mins_2     INTEGER DEFAULT 0,
    sancion_roja        INTEGER DEFAULT 0,
    sancion_azul        INTEGER DEFAULT 0
);

CREATE TABLE Estadisticas_Campo (
    id_estads_campo                     INTEGER PRIMARY KEY,
    id_base                             INTEGER NOT NULL UNIQUE REFERENCES Estadisticas_Base(id_base),
    -- totales
    total_lanzamientos                  INTEGER DEFAULT 0,
    total_goles                         INTEGER DEFAULT 0,
    total_paradas                       INTEGER DEFAULT 0,
    total_postes                        INTEGER DEFAULT 0,
    total_fuera                         INTEGER DEFAULT 0,
    total_bloqueados                    INTEGER DEFAULT 0,
    -- 7 metros
    m7_lanzamientos                     INTEGER DEFAULT 0,
    m7_goles                            INTEGER DEFAULT 0,
    m7_paradas                          INTEGER DEFAULT 0,
    m7_postes                           INTEGER DEFAULT 0,
    m7_fuera                            INTEGER DEFAULT 0,
    m7_bloqueados                       INTEGER DEFAULT 0,
    -- contraataque
    contraataque_lanzamientos           INTEGER DEFAULT 0,
    contraataque_goles                  INTEGER DEFAULT 0,
    contraataque_paradas                INTEGER DEFAULT 0,
    contraataque_postes                 INTEGER DEFAULT 0,
    contraataque_fuera                  INTEGER DEFAULT 0,
    contraataque_bloqueados             INTEGER DEFAULT 0,
    -- 9 metros
    m9_lanzamientos                     INTEGER DEFAULT 0,
    m9_goles                            INTEGER DEFAULT 0,
    m9_paradas                          INTEGER DEFAULT 0,
    m9_postes                           INTEGER DEFAULT 0,
    m9_fuera                            INTEGER DEFAULT 0,
    m9_bloqueados                       INTEGER DEFAULT 0,
    -- extremo
    extremo_lanzamientos                INTEGER DEFAULT 0,
    extremo_goles                       INTEGER DEFAULT 0,
    extremo_paradas                     INTEGER DEFAULT 0,
    extremo_postes                      INTEGER DEFAULT 0,
    extremo_fuera                       INTEGER DEFAULT 0,
    extremo_bloqueados                  INTEGER DEFAULT 0,
    -- 6 metros / pivote / contraataque posicional
    m6_lanzamientos                     INTEGER DEFAULT 0,
    m6_goles                            INTEGER DEFAULT 0,
    m6_paradas                          INTEGER DEFAULT 0,
    m6_postes                           INTEGER DEFAULT 0,
    m6_fuera                            INTEGER DEFAULT 0,
    m6_bloqueados                       INTEGER DEFAULT 0,
    -- valoración positiva
    valoracion_positiva_asistencia      INTEGER DEFAULT 0,
    valoracion_positiva_recuperacion    INTEGER DEFAULT 0,
    -- valoración negativa
    valoracion_negativa_perdida         INTEGER DEFAULT 0,
    valoracion_negativa_pasos           INTEGER DEFAULT 0,
    valoracion_negativa_dobles          INTEGER DEFAULT 0,
    valoracion_negativa_falta_ataque    INTEGER DEFAULT 0
);

CREATE TABLE Jugadores_rivales (
    id_jugador_rival    INTEGER PRIMARY KEY,
    id_equipo           INTEGER REFERENCES Equipos(id_equipo),
    nombre              VARCHAR(100),
    dorsal              INTEGER,
    posicion            VARCHAR(50)
);

CREATE TABLE Zonas_porteria (
    id_zona     INTEGER PRIMARY KEY,
    zona        INTEGER NOT NULL   -- 1-9, cuadrícula 3x3
);

CREATE TABLE Estadisticas_portero (
    id_base     INTEGER PRIMARY KEY REFERENCES Estadisticas_Base(id_base)
    -- ampliar con columnas de detalle según necesidad
);

CREATE TABLE Portero_situaciones (
    id_portero_sit      SERIAL PRIMARY KEY,   -- renombrado para evitar colisión con Situacion_Juego
    id_base             INTEGER NOT NULL REFERENCES Estadisticas_Base(id_base),
    situacion           VARCHAR(20) NOT NULL, --posicional,contra ,7m
    goles_recibidos     INTEGER DEFAULT 0,
    lanzamientos_totales INTEGER DEFAULT 0
);

CREATE TABLE Portero_mapa_detalle (
    id_mapa             SERIAL PRIMARY KEY,
    id_base             INTEGER NOT NULL REFERENCES Estadisticas_Base(id_base),
    id_zona             INTEGER NOT NULL REFERENCES Zonas_porteria(id_zona),
    id_jugador_rival    INTEGER REFERENCES Jugadores_rivales(id_jugador_rival),
    resultado_jugada    VARCHAR(20) NOT NULL  --gol, parada ,poste ,fuera
);


