--estadísticas totales de temporada por jugador de campo
CREATE OR REPLACE VIEW v_totales_campo AS
SELECT
    j.id_jugador,
    j.nombre_jugador,
    j.dorsal,
    p.rol_especifico                              AS posicion,
    COUNT(DISTINCT eb.id_partido)                 AS partidos_jugados,
    SUM(NULLIF(eb.tiempo_tot::INTEGER, 0))        AS minutos_totales,
    ROUND(SUM(eb.valoracion_global)::NUMERIC, 1)  AS valoracion_total,
    ROUND(AVG(eb.valoracion_global)::NUMERIC, 2)  AS valoracion_media,
    -- Lanzamientos
    SUM(ec.total_lanzamientos)                    AS total_lanzamientos,
    SUM(ec.total_goles)                           AS total_goles,
    CASE WHEN SUM(ec.total_lanzamientos) > 0
         THEN ROUND(SUM(ec.total_goles)::NUMERIC / SUM(ec.total_lanzamientos) * 100, 1)
         ELSE 0 END                               AS pct_efectividad,
    -- Por zonas
    SUM(ec.m9_lanzamientos)                       AS m9_lanz,
    SUM(ec.m9_goles)                              AS m9_goles,
    SUM(ec.extremo_lanzamientos)                  AS ext_lanz,
    SUM(ec.extremo_goles)                         AS ext_goles,
    SUM(ec.m6_lanzamientos)                       AS m6_lanz,
    SUM(ec.m6_goles)                              AS m6_goles,
    SUM(ec.m7_lanzamientos)                       AS m7_lanz,
    SUM(ec.m7_goles)                              AS m7_goles,
    -- Valoración positiva
    SUM(ec.valoracion_positiva_asistencia)        AS asistencias,
    SUM(ec.valoracion_positiva_recuperacion)      AS recuperaciones,
    -- Valoración negativa
    SUM(ec.valoracion_negativa_perdida)           AS perdidas,
    SUM(ec.valoracion_negativa_pasos)             AS pasos,
    SUM(ec.valoracion_negativa_dobles)            AS dobles,
    SUM(ec.valoracion_negativa_falta_ataque)      AS faltas_ataque,
    -- Sanciones
    SUM(eb.sancion_amarilla)                      AS tarjetas_amarillas,
    SUM(eb.sancion_2mins_1 + eb.sancion_2mins_2)  AS exclusiones_2min,
    SUM(eb.sancion_roja)                          AS tarjetas_rojas,
    SUM(eb.sancion_azul)                          AS tarjetas_azules
FROM Jugadores j
JOIN Posicion p          ON j.id_posicion = p.id_posicion
JOIN Estadisticas_Base eb ON j.id_jugador = eb.id_jugador
JOIN Estadisticas_Campo ec ON eb.id_base   = ec.id_base
WHERE p.categoria = 'Campo'
  AND eb.tiempo_tot IS NOT NULL
GROUP BY j.id_jugador, j.nombre_jugador, j.dorsal, p.rol_especifico
ORDER BY valoracion_total DESC;


-- vista porteros totales de temporada
CREATE OR REPLACE VIEW v_totales_porteros AS
SELECT
    j.id_jugador,
    j.nombre_jugador,
    j.dorsal,
    COUNT(DISTINCT eb.id_partido)                           AS partidos_jugados,
    SUM(NULLIF(eb.tiempo_tot::INTEGER, 0))                  AS minutos_totales,
    ROUND(SUM(eb.valoracion_global)::NUMERIC, 1)            AS valoracion_total,
    -- Totales de portero 
    SUM(ps.lanzamientos_totales)                            AS lanzamientos_recibidos,
    SUM(ps.goles_recibidos)                                 AS goles_recibidos,
    SUM(ps.lanzamientos_totales) - SUM(ps.goles_recibidos)  AS paradas,
    CASE WHEN SUM(ps.lanzamientos_totales) > 0
         THEN ROUND((SUM(ps.lanzamientos_totales) - SUM(ps.goles_recibidos))::NUMERIC
              / SUM(ps.lanzamientos_totales) * 100, 1)
         ELSE 0 END                                         AS pct_paradas,
    -- por situacion
    SUM(CASE WHEN ps.situacion='posicional'  THEN ps.goles_recibidos     ELSE 0 END) AS goles_posicional,
    SUM(CASE WHEN ps.situacion='posicional'  THEN ps.lanzamientos_totales ELSE 0 END) AS lanz_posicional,
    SUM(CASE WHEN ps.situacion='contra'      THEN ps.goles_recibidos     ELSE 0 END) AS goles_contra,
    SUM(CASE WHEN ps.situacion='contra'      THEN ps.lanzamientos_totales ELSE 0 END) AS lanz_contra,
    SUM(CASE WHEN ps.situacion='7m'          THEN ps.goles_recibidos     ELSE 0 END) AS goles_7m,
    SUM(CASE WHEN ps.situacion='7m'          THEN ps.lanzamientos_totales ELSE 0 END) AS lanz_7m,
    -- sanciones
    SUM(eb.sancion_amarilla)                                AS tarjetas_amarillas,
    SUM(eb.sancion_2mins_1 + eb.sancion_2mins_2)            AS exclusiones_2min
FROM Jugadores j
JOIN Estadisticas_Base     eb ON j.id_jugador  = eb.id_jugador
JOIN Estadisticas_portero  ep ON eb.id_base    = ep.id_base
JOIN Portero_situaciones   ps ON eb.id_base    = ps.id_base
WHERE eb.tiempo_tot IS NOT NULL
GROUP BY j.id_jugador, j.nombre_jugador, j.dorsal
ORDER BY minutos_totales DESC;


