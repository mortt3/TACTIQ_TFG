CREATE OR REPLACE VIEW v_resultados_zaragoza AS
SELECT
    pa.id_partido,
    pa.jornada,
    pa.fecha,
    pa.condicion,
    eq_l.nombre_equipo   AS equipo_local,
    pa.goles_local,
    pa.goles_visitante,
    eq_v.nombre_equipo   AS equipo_visitante,
    CASE
        WHEN pa.condicion = 'local'     AND pa.goles_local > pa.goles_visitante THEN 'Victoria'
        WHEN pa.condicion = 'visitante' AND pa.goles_visitante > pa.goles_local THEN 'Victoria'
        WHEN pa.goles_local = pa.goles_visitante                                THEN 'Empate'
        ELSE 'Derrota'
    END AS resultado,
    CASE pa.condicion
        WHEN 'local'     THEN pa.goles_local
        WHEN 'visitante' THEN pa.goles_visitante
    END AS goles_zaragoza,
    CASE pa.condicion
        WHEN 'local'     THEN pa.goles_visitante
        WHEN 'visitante' THEN pa.goles_local
    END AS goles_rival
FROM Partidos pa
JOIN Equipos eq_l ON pa.id_equipo_local     = eq_l.id_equipo
JOIN Equipos eq_v ON pa.id_equipo_visitante = eq_v.id_equipo
ORDER BY pa.jornada;

